public class NestedLoop {

    public static void main(String[] args) {

        System.out.println("This is What we call nested loop");
        for (int i = 0; i < 4; i++){
            System.out.println(i);

            for (int k = 0; k <4; k++){
                System.out.print("Q");
                System.out.print("is");
                System.out.print("not");
                System.out.print("Q");
            }

        }










    }









}
